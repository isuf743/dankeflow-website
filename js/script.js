  // 3D Card tilt effect
  const wrap = document.getElementById('hero3dWrap');
  const card = wrap ? wrap.querySelector('.hero-3d-card') : null;
  if (wrap && card) {
    let animating = false;

    wrap.addEventListener('mousemove', (e) => {
      const rect = wrap.getBoundingClientRect();
      const cx = rect.left + rect.width / 2;
      const cy = rect.top + rect.height / 2;
      const dx = (e.clientX - cx) / (rect.width / 2);
      const dy = (e.clientY - cy) / (rect.height / 2);
      const rx =  dy * -12;
      const ry =  dx *  14;
      card.style.animation = 'none';
      card.style.transform = `rotateX(${rx}deg) rotateY(${ry}deg) translateZ(8px)`;
      card.style.boxShadow = `
        ${-ry * 2}px ${rx * 2}px 60px rgba(0,0,0,0.7),
        0 0 80px rgba(59,130,246,${0.12 + Math.abs(dx) * 0.1}),
        inset 0 1px 0 rgba(255,255,255,0.06)
      `;
    });

    wrap.addEventListener('mouseleave', () => {
      card.style.animation = 'cardFloat 6s ease-in-out infinite';
      card.style.transform = '';
      card.style.boxShadow = '';
    });

    // Touch support for mobile
    wrap.addEventListener('touchmove', (e) => {
      const t = e.touches[0];
      const rect = wrap.getBoundingClientRect();
      const cx = rect.left + rect.width / 2;
      const cy = rect.top + rect.height / 2;
      const dx = (t.clientX - cx) / (rect.width / 2);
      const dy = (t.clientY - cy) / (rect.height / 2);
      card.style.animation = 'none';
      card.style.transform = `rotateX(${dy * -8}deg) rotateY(${dx * 10}deg) translateZ(4px)`;
    }, { passive: true });

    wrap.addEventListener('touchend', () => {
      card.style.animation = 'cardFloat 6s ease-in-out infinite';
      card.style.transform = '';
    });
  }

  function animateCounter(el){
    const target=parseFloat(el.dataset.count),prefix=el.dataset.prefix||'',isDecimal=target%1!==0,duration=1800,start=performance.now();
    function step(now){const ease=1-Math.pow(2,-10*Math.min((now-start)/duration,1)),cur=target*ease;el.textContent=prefix+(isDecimal?cur.toFixed(1):Math.floor(cur));if((now-start)<duration)requestAnimationFrame(step);else el.textContent=prefix+(isDecimal?target.toFixed(1):target);}
    requestAnimationFrame(step);
  }
  const counterObs=new IntersectionObserver(entries=>{entries.forEach(e=>{if(e.isIntersecting&&!e.target.dataset.done){e.target.dataset.done='1';animateCounter(e.target);counterObs.unobserve(e.target);}});},{threshold:0.5});
